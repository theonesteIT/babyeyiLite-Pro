'use strict';

/**
 * Generates 8 Excel files for student bulk import (one per P5 class).
 *
 * Usage:
 *   node scripts/generate-student-import-seeds.js
 *
 * Output: babyeyipro/Frontend/web/public/seed-students/students-import-P5A.xlsx ... P5H
 */

const path = require('path');
const fs = require('fs');
const XLSX = require('xlsx');

const CLASSES = ['P5A', 'P5B', 'P5C', 'P5D', 'P5E', 'P5F', 'P5G', 'P5H'];
const ACADEMIC_YEAR = '2025-2026';
const STUDENTS_PER_CLASS = 12;

const ROSTER_HEADERS = [
  'NO', 'Code', 'Student ID', 'Photo', 'Names', 'Gender', 'Age', 'grade',
  'Father Name', 'Mother Name', 'Father Phone', 'Mother Phone',
  'Province', 'District', 'Sector', 'Cell', 'Village',
];

const FIRST_NAMES = {
  M: ['Kevin', 'Eric', 'Patrick', 'Emmanuel', 'Fabrice', 'Olivier', 'Jean Paul', 'Samuel', 'Didier', 'Moise', 'Innocent', 'Cedric'],
  F: ['Grace', 'Claire', 'Alice', 'Chantal', 'Diane', 'Sandrine', 'Yvette', 'Ange', 'Joselyne', 'Mariam', 'Divine', 'Liliane'],
};

const LAST_NAMES = ['Mukamana', 'Uwase', 'Niyonzima', 'Habimana', 'Keza', 'Mugisha', 'Nshimiyimana', 'Umutoni', 'Bizimana', 'Nyirahabimana', 'Gasana', 'Iradukunda'];

const DISTRICTS = [
  { province: 'Kigali City', district: 'Gasabo', sector: 'Remera', cell: 'Rukiri', village: 'Amahoro' },
  { province: 'Kigali City', district: 'Kicukiro', sector: 'Kigarama', cell: 'Kigarama', village: 'Mataba' },
  { province: 'Kigali City', district: 'Nyarugenge', sector: 'Nyamirambo', cell: 'Mumena', village: 'Itaba' },
  { province: 'Southern Province', district: 'Huye', sector: 'Ngoma', cell: 'Matyazo', village: 'Karama' },
  { province: 'Northern Province', district: 'Musanze', sector: 'Muhoza', cell: 'Cyabararika', village: 'Gacaca' },
];

function padCode(classIdx, studentIdx) {
  const base = 250600000000 + classIdx * 100 + studentIdx;
  return String(base).padStart(12, '0');
}

function padStudentId(classIdx, studentIdx) {
  return `P5${String.fromCharCode(65 + classIdx)}${String(studentIdx + 1).padStart(3, '0')}`;
}

function buildRowsForClass(className, classIdx) {
  const rows = [];
  for (let i = 0; i < STUDENTS_PER_CLASS; i++) {
    const gender = i % 2 === 0 ? 'F' : 'M';
    const first = FIRST_NAMES[gender][i % FIRST_NAMES[gender].length];
    const last = LAST_NAMES[(classIdx + i) % LAST_NAMES.length];
    const loc = DISTRICTS[(classIdx + i) % DISTRICTS.length];
    const code = padCode(classIdx, i + 1);
    const studentId = padStudentId(classIdx, i);

    rows.push({
      NO: i + 1,
      Code: code,
      'Student ID': studentId,
      Photo: `${code}.jpg`,
      Names: `${last.toUpperCase()} ${first}`,
      Gender: gender,
      Age: 10 + (i % 3),
      grade: className,
      'Father Name': `${last} Pierre`,
      'Mother Name': `${last} Marie`,
      'Father Phone': `0788${String(100000 + classIdx * 1000 + i).slice(-6)}`,
      'Mother Phone': `0789${String(200000 + classIdx * 1000 + i).slice(-6)}`,
      Province: loc.province,
      District: loc.district,
      Sector: loc.sector,
      Cell: loc.cell,
      Village: loc.village,
    });
  }
  return rows;
}

function writeWorkbook(className, rows, outDir) {
  const wb = XLSX.utils.book_new();
  const ws = XLSX.utils.json_to_sheet(rows, { header: ROSTER_HEADERS });
  ws['!cols'] = ROSTER_HEADERS.map((h) => ({ wch: Math.max(14, h.length + 2) }));
  XLSX.utils.book_append_sheet(wb, ws, 'Class roster');

  const instructions = [
    ['Babyeyi — Student import (timetable test seed)'],
    [],
    ['Class file:', className],
    ['Academic year:', ACADEMIC_YEAR],
    [],
    ['Import steps (Students page):'],
    ['1. Set Class to', className],
    ['2. Set Academic year to', ACADEMIC_YEAR],
    ['3. Upload this file and click Import'],
    [],
    [`Contains ${rows.length} sample students for timetable testing.`],
  ];
  const instrWs = XLSX.utils.aoa_to_sheet(instructions);
  instrWs['!cols'] = [{ wch: 70 }];
  XLSX.utils.book_append_sheet(wb, instrWs, 'Instructions');

  const filePath = path.join(outDir, `students-import-${className}.xlsx`);
  XLSX.writeFile(wb, filePath);
  return filePath;
}

function main() {
  const outDir = path.resolve(__dirname, '..', '..', '..', 'babyeyipro', 'Frontend', 'web', 'public', 'seed-students');
  fs.mkdirSync(outDir, { recursive: true });

  const written = [];
  CLASSES.forEach((className, classIdx) => {
    const rows = buildRowsForClass(className, classIdx);
    written.push(writeWorkbook(className, rows, outDir));
  });

  const readme = `# Student import seed files (timetable test)

Generated by: \`node scripts/generate-student-import-seeds.js\`

## Files
${CLASSES.map((c) => `- students-import-${c}.xlsx (${STUDENTS_PER_CLASS} students)`).join('\n')}

## Import on Students page
1. Open **Manager → Students**
2. For each file:
   - **Class**: P5A (match the file name)
   - **Academic year**: ${ACADEMIC_YEAR}
   - Upload the matching \`students-import-P5X.xlsx\`
   - Click **Import**

## After import
1. Run timetable seed: \`node scripts/seed-timetable-demo.js\`
2. DOS → Timetable → Generator → select all P5 classes → Generate → Apply
3. Conflict Center → Scan
`;

  fs.writeFileSync(path.join(outDir, 'README.md'), readme, 'utf8');

  console.log('\nGenerated student import files:');
  written.forEach((p) => console.log('  ', p));
  console.log(`\nImport each file with Class = P5A…P5H and Academic year = ${ACADEMIC_YEAR}\n`);
}

main();
